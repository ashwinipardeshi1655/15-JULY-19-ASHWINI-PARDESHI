public class ArithmaticOperators{
	public static void main(String[]args){
		int i = 20 ;
		int j = 10 ;
		int res = i + j ;                                 //Initialization
		System.out.println("Sum = "+res);                 //Addition      
		
		int sub = i - j ;                                 //SUBTRACTION
		System.out.println("subtraction = "+sub);
		
		int mult = i * j ;                                //MULTIPLICATION
		System.out.println("Multiply = "+mult);
		
		int divide = i/j ;                                //DIVISION
		System.out.println("Divide = "+divide);
		
		int percentile = i%j ;                           //MODULUS
		System.out.println("percentile = "+percentile);
		
		char a = 'a' ;
		char b = 'b' ;
		int charsum = a + b;
		System.out.println("charsum = "+charsum);
		
		int charsub = a - b;
		System.out.println("charsub = "+charsub);
		
		int charmult = a * b ;
		System.out.println("charmult = "+charmult);
		
		char x = 'a';
		int y = 10 ; 
		int icsum = x + y;
		System.out.println("icsum = "+icsum);
		
		char u = 'A';
		double v = 123456 ; 
		double cdsum = u + v;
		System.out.println("cdsum = "+cdsum);
		
		int m = 34;
		double n = 123456 ; 
		double idsum = m + n;
		System.out.println("idsum = "+idsum);
		
}}